var readlineSync=require('readline-sync');
var num=readlineSync.question('Enter the number');
switch(num)
{
    case "1":
        console.log('unit');
    break;

    case "1":
        console.log('unit');
    break;

    case "2":
        console.log('TEN');
    break;

    case "3":
        console.log('HUNDRED');
    break;

    case "4":
        console.log('THOUSAND');
    break;

    case "5":
        console.log('TEN THOUSAND');
    break;
}